# integer conversion
num1 = int("100")
num2 = int(50.50)
num3 = int(True)
num4 = int(False)

print(num1,num2,num3,num4)

# float conversion
float_num1 = float("60.66")
float_num2 = float(50)
float_num3 = float(True)
float_num4 = float(False)

print(float_num1,float_num2,float_num3,float_num4)


# string conversation
str1 = str(100)
str2 = str(50.50)
str3 = str(True)
str4 = str(False)

print(str1,str2,str3,str4)

# boolean conversion
bool1 = bool(1)
bool2 = bool(0)
bool3 = bool(50)
bool4 = bool(50.50)
bool5 = bool("")
bool6 = bool("Hello")

print(bool1,bool2,bool3,bool4,bool5,bool6)
